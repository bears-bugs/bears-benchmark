DROP DATABASE H2MSDatabase;
CREATE DATABASE H2MSDatabase;
USE H2MSDatabase;

CREATE TABLE DOCTOR (
       ID INT(10) NOT NULL AUTO_INCREMENT
     , FIRST_NAME VARCHAR(40) NOT NULL,
     , LAST_NAME VARCHAR(40) NOT NULL
     , PRIMARY KEY (ID)
);

CREATE TABLE EVENT (
       ID INT(10) NOT NULL AUTO_INCREMENT
     , HAND_WASH_TYPE VARCHAR(50) NOT NULL
     , RELATIVE_MOMENT VARCHAR(50) NOT NULL
     , OBSERVEE VARCHAR(50) NOT NULL
     , OBSERVER VARCHAR(50) NOT NULL
     , OBSERVATION_TYPE VARCHAR(50) NOT NULL
     , PRIMARY KEY (ID)
);

CREATE TABLE LOCATION (
       ID INT(10) NOT NULL AUTO_INCREMENT
     , HOSPITAL_NAME VARCHAR(50) NOT NULL
     , WARD_NAME VARCHAR(50) NOT NULL
     , PRIMARY KEY (ID)
);

CREATE TABLE READER (
       ID INT(10) NOT NULL AUTO_INCREMENT
     , ORDER_NUMBER DECIMAL(4,2) NOT NULL
     , BRAND_NAME VARCHAR(50) NOT NULL
     , MODEL_NUMBER VARCHAR(50) NOT NULL
     , CAPACITY VARCHAR(50) NOT NULL
     , VOLTAGE VARCHAR(50) NOT NULL
     , CHARGE_TIME VARCHAR(50) NOT NULL
     , PRIMARY KEY (ID)
);

CREATE TABLE WRIST_BAND (
       ID INT(10) NOT NULL AUTO_INCREMENT
     , SKU VARCHAR(50) NOT NULL
     , BIT_FORMAT VARCHAR(50) NOT NULL
     , COLOR VARCHAR(50) NOT NULL
     , ORDER_NUMBER DECIMAL(4,2) NOT NULL
     , LASER_ENGRAVED VARCHAR(50) NOT NULL
     , PRIMARY KEY (ID)
);