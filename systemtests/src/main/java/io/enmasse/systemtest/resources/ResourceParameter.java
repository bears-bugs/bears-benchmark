package io.enmasse.systemtest.resources;

public class ResourceParameter {
    private String name;
    private String value;

    public ResourceParameter(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }
}
